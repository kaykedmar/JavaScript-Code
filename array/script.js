let fruits = ["apple", "Banana", "Orange"]; 

// Adiciona "Morango" como um novo elemento ao final da lista de frutas usando o método push().
fruits.push ("Morango")

// Remove o último elemento da lista de frutas usando o método pop().
fruits.pop();

// Adiciona "Abacaxi" como o primeiro elemento na lista de frutas usando o método unshift().
fruits.unshift("Abacaxi")

// Remove o primeiro elemento da lista de frutas usando o método shift().
fruits.shift()

// Obtém a quantidade de elementos no array "fruits".
let length = fruits.length; 

// Descobre a posição (índice) do elemento "Morango" no array "fruits".
let index = fruits.indexOf("Morango")


